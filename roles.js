function openRoles() {
  document.getElementsByClassName("col-md-4 app-panel background-gray")[0].style.display = "block";
  document.getElementsByClassName("panel-wrapper")[0].className = "panel-wrapper background-gray";
}

function openAssignment() {
  document.getElementsByClassName("col-md-4 app-panel background-white")[0].style.display = "block";
  document.getElementsByClassName("panel-wrapper")[0].className = "panel-wrapper background-white";
}
